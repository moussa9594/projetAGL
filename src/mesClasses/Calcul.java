package mesClasses;

public class Calcul {

	//Une méthodes addition qui prend en entrée deux nombres et retourne le résultat
		public double addition (double x , double y)
		{
			return x + y ;
		}
		
	//Une méthodes multiplication qui prend en entrée deux nombres et retourne le résultat.
		public double multiplication (double x , double y)
		{
			return x * y ;
		}
	
}
